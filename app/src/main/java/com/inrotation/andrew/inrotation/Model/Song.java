package com.inrotation.andrew.inrotation.Model;

public class Song {

    String songName;
    String artist;
    String album;
    int duration;



    public Song(String songName, String artist, String album, int duration) {
        this.songName = songName;
        this.artist = artist;
        this.album = album;
        this.duration = duration;
    }





}